    void HoverMenuleft() {
        JLabel[] labels = {btnqlcp, btnqlhd, btnqllp, btnqlnv, btnqlp, btnqltk, btntk, btntp, btntc, btnqlkh};
        for (JLabel label : labels) {
            label.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseEntered(MouseEvent e) {
                    // Xử lý khi chuột di chuyển vào label 
                    label.setForeground(Color.red); // Hiển thị label  như một link
                }

                @Override
                public void mouseExited(MouseEvent e) {
                    // Xử lý khi chuột di chuyển ra khỏi label 
                    label.setForeground(Color.white); // Trở lại trạng thái ban đầu của label 
                }
            });
        }
    }
    

