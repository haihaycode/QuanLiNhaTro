    public void displayCurrentTime() {
        new Thread(() -> {
            while (true) {
                ZonedDateTime currentDateTime = ZonedDateTime.now();
                // Định dạng thời gian theo định dạng quốc gia, ngày tháng năm và giờ (tiếng Việt)
                String formattedDateTime = DateTimeFormatter.ofLocalizedDateTime(FormatStyle.FULL)
                        .withLocale(new Locale("vi", "VN"))
                        .format(currentDateTime);
                updateUITime(formattedDateTime);
                try {
                    Thread.sleep(1000); // Tạm dừng 1 giây trước khi cập nhật thời gian tiếp theo
                } catch (InterruptedException e) {
                    System.out.println(e.getMessage() + "Lỗi hàm currentTime");
                }
            }
        }).start();
    }

    void updateUITime(String time) {
        // Cập nhật thời gian hiện tại lên giao diện người dùng tại đây
        txtcurrenttime.setText("" + time);
    }

